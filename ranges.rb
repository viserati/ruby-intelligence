=begin
A range is A range is two values surrounded by parentheses and separated by
an ellipsis (in the form of two or three dots).
=end

(1..3) is a range, representing the numbers 1 through 3.

# Normally, only two dots are used. If a third dot is used, the last value in
# the range is excluded.

(0...5) represents the numbers 0 through 4.
